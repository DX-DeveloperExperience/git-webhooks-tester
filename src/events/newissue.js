prerequisite = async git => {
  const inquirer = require('inquirer');

  return await inquirer
    .prompt([
      {
        type: 'input',
        name: 'title',
        message: 'IssueTitle?',
      },
      {
        type: 'input',
        name: 'description',
        message: 'IssueDescription?',
      },
    ])
    .then(answers => {
      if (git === 'github') {
        github.issue.title = answers.title;
        github.issue.body = answers.description;
        return github;
      }
      if (git === 'gitlab') {
        gitlab.object_attributes.title = answers.title;
        gitlab.object_attributes.description = answers.description;
        return gitlab;
      }
    });
};

module.exports = git => {
  return prerequisite(git);
};
