prerequisite = async git => {
  const inquirer = require('inquirer');

  return await inquirer
    .prompt([
      {
        type: 'input',
        name: 'comment',
        message: 'IssueComment?',
      },
    ])
    .then(answer => {
      if (git === 'github') {
        github.comment.body = answer.comment;
        return github;
      }
      if (git === 'gitlab') {
        gitlab.object_attributes.description = answer.comment;
        return gitlab;
      }
    });
};

module.exports = git => {
  return prerequisite(git);
};
