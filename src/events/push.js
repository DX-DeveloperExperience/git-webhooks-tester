prerequisite = async git => {
  const inquirer = require('inquirer');

  return await inquirer
    .prompt([
      {
        type: 'input',
        name: 'message',
        message: 'Commit Message?',
      },
    ])
    .then(answer => {
      if (git === 'github') {
        github.commits[0].message = answer.message;
        return github;
      }
      if (git === 'gitlab') {
        gitlab.commits[0].message = answer.message;
        return gitlab;
      }
    });
};

module.exports = git => {
  return prerequisite(git);
};
