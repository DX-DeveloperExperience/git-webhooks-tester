prerequisite = async git => {
  const inquirer = require('inquirer');

  return await inquirer
    .prompt([
      {
        type: 'input',
        name: 'title',
        message: 'PRTitle?',
      },
      {
        type: 'input',
        name: 'description',
        message: 'PRDescription?',
      },
    ])
    .then(answers => {
      if (git === 'github') {
        github.pull_request.title = answers.title;
        github.pull_request.body = answers.description;
        return github;
      }
      if (git === 'gitlab') {
        gitlab.object_attributes.title = answers.title;
        gitlab.object_attributes.description = answers.description;
        return gitlab;
      }
    });
};

module.exports = git => {
  return prerequisite(git);
};
