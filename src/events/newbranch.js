prerequisite = async git => {
  const inquirer = require('inquirer');

  return await inquirer
    .prompt([
      {
        type: 'input',
        name: 'branch',
        message: 'BranchName?',
      },
    ])
    .then(answer => {
      if (git === 'github') {
        github.ref = answer.branch;
        return github;
      }
      if (git === 'gitlab') {
        gitlab.ref = 'refs/heads/' + answer.branch;
        return gitlab;
      }
    });
};

module.exports = git => {
  return prerequisite(git);
};
